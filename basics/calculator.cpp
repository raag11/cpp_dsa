#include <iostream>
using namespace std;
int main()
{
    float a, b;
    cout << "Enter First Number: \n";
    cin >> a;
    cout << "Enter Second Number: \n";
    cin >> b;
    cout << "The sum is : " << a + b << "\n";
    cout << "The difference is : " << a - b << "\n";
    cout << "The product is : " << a * b << "\n";
    cout << "The division is : " << a / b << "\n";
    return 0;
}